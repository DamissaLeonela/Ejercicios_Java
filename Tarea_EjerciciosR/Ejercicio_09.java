package Tarea_EjerciciosR;

/*
 * Numeros del 1 al 20 en una misma fila:
 */

public class Ejercicio_09 {

    public static void main(String[] args) {
        
        int i = 1;
        do { 
            System.out.print( i + " " );
            i++;
        
        } while (i <= 20);   

        System.out.println();
    }
}
