package Tarea_EjerciciosR;

/*
 * Numeros del 100 al 1:
 */

public class Ejercicio_05 {

    public static void main(String[] args) {

        System.out.println("Numeros del 100 al 1: ");                                                             
        int i=100;
         
        while(i>=1) {

            System.out.println(i);
            i--;

        }
    }   
}
